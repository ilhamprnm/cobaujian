export const shuffleAnswers = (questions) => {
  const unshuffledAnswers = [
    questions.Jawaban,
    ...questions.JawabanSalah,
  ];

  return unshuffledAnswers
    .map((answer) => ({sort: Math.random(), value: answer}))
    .sort((a, b) => a.sort - b.sort)
    .map((obj) => obj.value)
}

